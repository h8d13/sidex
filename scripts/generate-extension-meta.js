import { readdirSync, readFileSync, writeFileSync, existsSync, statSync } from 'fs';
import { join, resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const extensionsDir = resolve(__dirname, '..', 'extensions');
const metaOutputPath = resolve(__dirname, '..', 'extensions-meta.json');
const jsOutputPath = resolve(__dirname, '..', 'public', 'builtin-extensions.js');

if (!existsSync(extensionsDir)) {
  console.error(`Extensions directory not found: ${extensionsDir}`);
  process.exit(1);
}

const essentialPkgFields = [
  'name', 'displayName', 'description', 'version', 'publisher', 'license',
  'engines', 'categories', 'contributes', 'activationEvents', 'main', 'browser',
  'extensionKind', 'extensionPack', 'extensionDependencies', 'icon',
  'enabledApiProposals', 'capabilities',
];

function stripPackageJson(pkg) {
  const stripped = {};
  for (const key of essentialPkgFields) {
    if (pkg[key] !== undefined) {
      stripped[key] = pkg[key];
    }
  }
  return stripped;
}

const entries = readdirSync(extensionsDir);
const descriptors = [];

for (const dirName of entries) {
  const dirPath = join(extensionsDir, dirName);
  if (!statSync(dirPath).isDirectory()) continue;

  const pkgPath = join(dirPath, 'package.json');
  if (!existsSync(pkgPath)) continue;

  let packageJSON;
  try {
    packageJSON = JSON.parse(readFileSync(pkgPath, 'utf-8'));
  } catch (err) {
    console.warn(`Skipping ${dirName}: failed to parse package.json — ${err.message}`);
    continue;
  }

  let packageNLS = undefined;
  const nlsPath = join(dirPath, 'package.nls.json');
  if (existsSync(nlsPath)) {
    try {
      packageNLS = JSON.parse(readFileSync(nlsPath, 'utf-8'));
    } catch {
      // nls is optional, ignore parse errors
    }
  }

  const descriptor = { extensionPath: dirName, packageJSON: stripPackageJson(packageJSON) };
  if (packageNLS) descriptor.packageNLS = packageNLS;
  descriptors.push(descriptor);
}

descriptors.sort((a, b) => a.extensionPath.localeCompare(b.extensionPath));

writeFileSync(metaOutputPath, JSON.stringify(descriptors, null, 2));
console.log(`Wrote ${descriptors.length} extension descriptors to extensions-meta.json`);

const jsContent = `// Auto-generated by scripts/generate-extension-meta.js — do not edit
(function() {
  globalThis._VSCODE_BUILTIN_EXTENSIONS = ${JSON.stringify(descriptors)};
})();
`;

writeFileSync(jsOutputPath, jsContent);
console.log(`Wrote public/builtin-extensions.js (${(jsContent.length / 1024).toFixed(1)} KB)`);
